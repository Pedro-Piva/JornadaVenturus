package Atividade4;

/**
 *
 * @author pedro
 */
public class Itens {

    private int codigo;
    private String produto;
    private double preco;

    public Itens(int codigo, String prod, double preco) {
        this.codigo = codigo;
        this.produto = prod;
        this.preco = preco;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getProduto() {
        return produto;
    }

    public void setProduto(String produto) {
        this.produto = produto;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
    
}
